export function generateWordText(difficulty: string, wordCount = 50): string {
  const pool = wordPool[difficulty as keyof typeof wordPool] || wordPool.Easy;
  const words: string[] = [];
  for (let i = 0; i < wordCount; i++) {
    words.push(pool[Math.floor(Math.random() * pool.length)]);
  }
  return words.join(" ");
}
